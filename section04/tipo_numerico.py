"""
Tipo Numérico

** Potência
// Divisão inteira
//= Divisão inteira atribuída
"""

# num = 1_000_000

num = 1000000
print(num)
print(float(num))
print(type(num), "\n")

num //= 10000
print(num)
print(type(num), "\n")

# Ao dividir um número inteiro ele automaticamente será convertido em float
num /= 100
print(num)
print(type(num))
